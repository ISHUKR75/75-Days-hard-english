function useDayData(dayNum) {
  const [data, setData] = useState(null);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    async function loadData() {
      try {
        setLoading(true);
        // Dynamically import the massive JSON payload generated by scripts/generate_all_days.js
        // We use a relative path trick or fetch if this was served as static asset.
        // In Next.js, importing from outside the project tree can be tricky if it's dynamic.
        // Assuming we fetch from an API route or it's accessible.
        // For local development, we will fetch it assuming we expose a simple API or just statically import it if possible.
        // The safest robust way in Next.js for client components to read massive local JSON files is an API route.
        const res = await fetch(`/api/challenge/${dayNum}`);
        if (res.ok) {
          const json = await res.json();
          setData(json);
        } else {
          console.error("Failed to load day data, using fallback.");
        }
      } catch (err) {
        console.error(err);
      } finally {
        setLoading(false);
      }
    }
    loadData();
  }, [dayNum]);

  return { data, loading };
}

